package Homework.lesson_27.game_character;

/**
 * Интерфейс для управления целями персонажей.
 * Определяет базовые методы для установки и получения текущей цели,
 * которую персонаж может атаковать или лечить.
 *
 * Реализация этого интерфейса гарантирует, что класс сможет:
 * - Иметь целевую сущность для взаимодействия
 * - Динамически менять цель во время игры
 * - Интегрироваться в систему боевых взаимодействий
 */
public interface Targetable {

    /**
     * Устанавливает целевую сущность для взаимодействия
     * @param target Игровой персонаж, который будет целью
     *              (может быть противником для атаки или союзником для лечения)
     */
    void setTarget(GameCharacter target);

    /**
     * Возвращает текущую цель персонажа
     * @return Текущий целевой игровой персонаж или null, если цель не установлена
     */
    GameCharacter getTarget();
}
