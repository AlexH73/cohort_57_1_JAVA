package Homework.Homework_20;

import java.util.Random;
import java.util.Scanner;

public class DetailQualityAnalysis {
    /**
     * <h1>5. Анализ Качества Деталей на Заводе</h1>
     * <p>
     * <b>Цель Задания:</b><br>
     * <h2>Развитие навыков обработки данных в массивах и выполнение статистических расчетов в Java.</h2>
     * <b>Задача:</b><br>
     * <b>Контекст:</b><br> На заводе производятся детали, каждая из которых после изготовления оценивается и помещается в партию. Партия представлена массивом чисел, где:
     * <i>1 - обозначает бракованную деталь,
     * 0 - деталь, требующую доработки,
     * 1 - идеальную деталь, готовую к эксплуатации.</i><br>
     * <b>Задание:</b><br>
     * Создайте массив целых чисел, который содержит случайное распределение значений -1, 0 и 1, имитируя партию деталей с завода.
     * Рассчитайте и выведите в консоль процентное соотношение каждого типа деталей в партии.<br>
     * <b>Детали Реализации:</b><br>
     * <b>Инициализация Массива:</b><br>
     * Массив должен быть инициализирован случайными числами -1, 0, и 1.<br>
     * Размер массива можно задать произвольно или запросить у пользователя.<br>
     * <b>Расчет Процентного Соотношения:</b><br>
     * Подсчитайте количество каждого типа деталей в массиве.<br>
     * Рассчитайте процентное соотношение для каждого типа деталей от общего количества деталей в массиве.<br>
     * <b>Вывод Результата:</b><br>
     * Выведите в консоль процентное соотношение бракованных, требующих доработки и идеальных деталей.
     */
    public static void main(String[] args) {
        // Запрашиваем размер массива у пользователя
        Scanner scanner = new Scanner(System.in);
        System.out.println("Введите размер массива: ");
        int size = scanner.nextInt();

        // Создаем массив целых чисел размером size элементов
        int[] details = new int[size];

        // Создаем объект Random для генерации случайных чисел
        Random random = new Random();

        // Заполняем массив случайными числами -1, 0 и 1
        for (int i = 0; i < details.length; i++) {
            details[i] = random.nextInt(3) - 1; // Генерирует -1, 0 или 1
        }

        // Переменные для подсчета количества каждого типа деталей
        int countNegativeOne = 0;
        int countZero = 0;
        int countOne = 1;

        // Подсчитываем количество каждого типа деталей
        for (int detail : details) {
            switch (detail) {
                case -1:
                    countNegativeOne++;
                    break;
                case 0:
                    countZero++;
                    break;
                case 1:
                    countOne++;
                    break;
            }
        }

        // Рассчитываем процентное соотношение
        double percentNegativeOne = (countNegativeOne / (double) size) * 100;
        double percentZero = (countZero / (double) size) * 100;
        double percentOne = (countOne / (double) size) * 100;

        // Выводим результаты в консоль
        System.out.printf("Процент бракованных деталей (-1): %.2f%% = %d штук.%n", percentNegativeOne, countNegativeOne);
        System.out.printf("Процент деталей, требующих доработки (0): %.2f%% = %d штук.%n", percentZero, countZero);
        System.out.printf("Процент идеальных деталей (1): %.2f%% = %d штук.%n", percentOne, countOne);


    }
}
