package Lessons.Lesson_33.code;

import java.util.Queue;

/**
 * Интерфейс для управления системой обработки звонков в колл-центре.
 * Входящие звонки добавляются в очередь и обрабатываются операторами в порядке поступления (FIFO).
 */
public interface CallCenterQueue {

    /**
     * Добавляет новый входящий звонок в очередь.
     *
     * @param callerName имя звонящего
     * @param phoneNumber номер телефона звонящего
     */
    void addCall(String callerName, String phoneNumber);

    /**
     * Обрабатывает следующий звонок из очереди.
     * Если очередь пуста, метод должен вывести сообщение, что звонков в ожидании нет.
     *
     * @return информацию о звонке, который был обработан, или сообщение об отсутствии звонков
     */
    String handleNextCall();

    /**
     * Проверяет, есть ли в очереди ожидающие звонки.
     *
     * @return true, если в очереди есть звонки, иначе false
     */
    boolean hasPendingCalls();

    /**
     * Возвращает текущее количество ожидающих звонков в очереди.
     *
     * @return количество звонков в очереди
     */
    int getPendingCallCount();

    /**
     * Отображает все ожидающие звонки в очереди.
     * Должен выводить список звонков в формате: "Звонящий: [Имя] - Телефон: [Номер]"
     */
    void displayPendingCalls();
}