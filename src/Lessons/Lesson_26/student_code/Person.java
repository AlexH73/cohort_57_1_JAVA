package Lessons.Lesson_26.student_code;

public class Person {
    private int age;
    private String name;
    private String phoneNumber;

    public Person(int age, String name, String phoneNumber) {
        this.age = age;
        this.name = name;
        this.phoneNumber = phoneNumber;
    }


    public String getName() {
        return name;
    }

}
