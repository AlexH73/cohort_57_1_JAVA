package Homerwork.Practica23;

public class User {
    private String username;
    private String email;
    private int age;

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public int getAge() {
        return age;
    }

    public void setUsername() {
        this.username = username;
    }

    public void setEmail() {
        this.email = email;
    }

    public void setAge() {
        this.age = age;
    }
}
