package Homerwork.Practica.Practica19;

public class DataTypesDemo {
    public static void main(String[] args) {
        //Объявление и инициализация переменных
        int number = 22;
        double price = 55.99;
        boolean isJavaFun = true;
        char letter = 'J';  // символьная переменная хранящая одиночный символ
        String message = "Привет, Java!";

        //Вывод значений в консоль
        System.out.println("Целое число (int): " + number);
        System.out.println("Дробное число (double): " + price);
        System.out.println("Логическое значение (boolean): " + isJavaFun);
        System.out.println("Символ (char): " + letter);
        System.out.println("Строка (String): " + message);
    }
}
