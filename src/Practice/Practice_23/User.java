public class User {
    private String username;
    private String email;
    private int age;

    // Геттер для username
    public String getUsername() {
        return username;
    }

    // Сеттер для username
    public void setUsername(String username) {
        this.username = username;
    }

    // Геттер для email
    public String getEmail() {
        return email;
    }

    // Сеттер для email
    public void setEmail(String email) {
        this.email = email;
    }

    // Геттер для age
    public int getAge() {
        return age;
    }

    // Сеттер для age
    public void setAge(int age) {
        this.age = age;
    }
}
