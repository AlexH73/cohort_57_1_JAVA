public class Book {
    private String title;
    private String author;
    private int pages;

    // Геттер для title
    public String getTitle() {
        return title;
    }

    // Сеттер для title
    public void setTitle(String title) {
        this.title = title;
    }

    // Геттер для author
    public String getAuthor() {
        return author;
    }

    // Сеттер для author
    public void setAuthor(String author) {
        this.author = author;
    }

    // Геттер для pages
    public int getPages() {
        return pages;
    }

    // Сеттер для pages
    public void setPages(int pages) {
        this.pages = pages;
    }
}
